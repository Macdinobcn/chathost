export type TranslationKey = keyof typeof translations.es

// Helper: obtiene las traducciones para un idioma dado
export function useT(lang: Idioma = 'es') {
  const t = translations[lang] ?? translations.es
  // Función para reemplazar variables como {n}
  return function tr(key: TranslationKey, vars?: Record<string, string | number>): string {
    let str = (t as any)[key] ?? (translations.es as any)[key] ?? key
    if (vars) {
      Object.entries(vars).forEach(([k, v]) => {
        str = str.replace(`{${k}}`, String(v))
      })
    }
    return str
  }
}

// Formatea moneda según el idioma
export function formatCurrency(amount: number, lang: Idioma = 'es'): string {
  const locale: Record<Idioma, string> = {
    es: 'es-ES', en: 'en-GB', fr: 'fr-FR', de: 'de-DE', nl: 'nl-NL',
  }
  return new Intl.NumberFormat(locale[lang], {
    style: 'currency',
    currency: 'EUR',
    minimumFractionDigits: 2,
    maximumFractionDigits: 4,
  }).format(amount)
}

// Formatea números según el idioma
export function formatNumber(n: number, lang: Idioma = 'es'): string {
  const locale: Record<Idioma, string> = {
    es: 'es-ES', en: 'en-GB', fr: 'fr-FR', de: 'de-DE', nl: 'nl-NL',
  }
  return new Intl.NumberFormat(locale[lang]).format(n)
}
